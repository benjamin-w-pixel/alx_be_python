# simple_interest.py

# Define three variables: principal, rate, and time.
principal = 1000  # Represents $1000
rate = 0.05       # Represents 5% annual interest rate (as a decimal)
time = 3          # Represents 3 years

# Calculate the simple interest using the formula I = P * R * T
# Store the result in a variable named interest.
interest = principal * rate * time

# Print the calculated interest in the format: The simple interest is: [interest].
print(f"The simple interest is: {interest}")
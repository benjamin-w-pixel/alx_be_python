# hours_to_seconds.py

# Define a variable named hours and assign it a value.
hours = 2

# Calculate the number of seconds in the given hours.
# Remember, there are 3600 seconds in an hour (60 minutes * 60 seconds).
seconds = hours * 3600

# Print the result in the format: [hours] hour(s) is [seconds] seconds.
print(f"{hours} hour(s) is {seconds} seconds.")